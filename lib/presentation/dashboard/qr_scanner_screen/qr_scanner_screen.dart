class CornerBorderBox extends StatelessWidget {
  final Rect boxRect;

  CornerBorderBox({Key? key, required this.boxRect}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return CustomPaint(
      painter: CornerBorderPainter(boxRect),
    );
  }
}

class CornerBorderPainter extends CustomPainter {
  final Rect rect;
  CornerBorderPainter(this.rect);

  @override
  void paint(Canvas canvas, Size size) {
    final paint = Paint()
      ..color = Colors.white
      ..strokeWidth = 3.0
      ..style = PaintingStyle.stroke;

    double cornerLength = 20.0;

    // Draw top-left corner
    canvas.drawLine(
        rect.topLeft, rect.topLeft.translate(cornerLength, 0), paint);
    canvas.drawLine(
        rect.topLeft, rect.topLeft.translate(0, cornerLength), paint);

    // Draw top-right corner
    canvas.drawLine(
        rect.topRight, rect.topRight.translate(-cornerLength, 0), paint);
    canvas.drawLine(
        rect.topRight, rect.topRight.translate(0, cornerLength), paint);

    // Draw bottom-left corner
    canvas.drawLine(
        rect.bottomLeft, rect.bottomLeft.translate(cornerLength, 0), paint);
    canvas.drawLine(
        rect.bottomLeft, rect.bottomLeft.translate(0, -cornerLength), paint);

    // Draw bottom-right corner
    canvas.drawLine(
        rect.bottomRight, rect.bottomRight.translate(-cornerLength, 0), paint);
    canvas.drawLine(
        rect.bottomRight, rect.bottomRight.translate(0, -cornerLength), paint);
  }

  @override
  bool shouldRepaint(CornerBorderPainter oldDelegate) {
    return oldDelegate.rect != rect;
  }
}

// Function that returns Rect and builds the widget
Rect createCornerBorderBoxWidget(
    BuildContext context, Offset position, Size boxSize) {
  final boxRect =
      Rect.fromLTWH(position.dx, position.dy, boxSize.width, boxSize.height);
  CornerBorderBox(
      boxRect:
          boxRect); // Creates the widget but does not render it directly here
  return boxRect;
}
